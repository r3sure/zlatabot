def detect_gender(name: str) -> str | None:
    name_clean = name.strip().lower()
    if name_clean in MALE_NAMES:
        return "male"
    if name_clean in FEMALE_NAMES:
        return "female"
    # Check endings as fallback for unknown names
    if name_clean.endswith(("а", "я", "ия", "ья", "ля", "на", "и")):
        return "female"
    if name_clean.endswith(("й", "ь", "н", "д", "р", "л", "м", "г", "к", "х", "в", "с", "у", "ш", "ч")):
        return "male"
    return None


def capitalize_name(name: str) -> str:
    if not name:
        return name
    return name.strip().capitalize()
