POSITIONS = [
    ("левый верхний", "потенциал, таланты"),
    ("правый верхний", "поддержка, окружение"),
    ("левый нижний", "опыт, внутренняя опора"),
    ("правый нижний", "развитие, рост"),
    ("центр", "баланс, гармония"),
]


def _reduce(n: int) -> int:
    while n > 22:
        s = sum(int(d) for d in str(n))
        if s <= 22:
            return s
        n = s
    return n


def _sum_digits(n: int) -> int:
    return sum(int(d) for d in str(n))


def calculate_matrix(day: int, month: int, year: int) -> list[dict]:
    a1 = _reduce(day)
    a2 = _reduce(month)
    a3 = _reduce(year)
    a4 = _reduce(_sum_digits(day) + _sum_digits(month) + _sum_digits(year))
    center = _reduce(a1 + a2 + a3 + a4)

    arcs = [a1, a2, a3, a4, center]
    result = []
    for (pos_name, pos_desc), arc_num in zip(POSITIONS, arcs):
        result.append({
            "position": pos_name,
            "meaning": pos_desc,
            "arcana": arc_num,
            "name": ARCA_NA[arc_num],
            "description": ARCA_DESC[arc_num],
        })
    return result
